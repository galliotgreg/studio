function themeToCss(theme: { [key: string]: string }) {
    return Object.entries(theme).reduce((acc, [key, value]) => {
        acc[key.replace(/^--/, '')] = `hsl(var(${key}))`;
        return acc;
    }, {} as { [key: string]: string });
}

